class Calculadora {
    fun soma (a: Double, b: Double): Double{
        return a+b
    }
    fun subtracao (a: Double, b: Double ): Double{
        return a-b
    }
    fun multiplicacao (a: Double, b: Double): Double{
        return a*b
    }
    fun divisao (a: Double, b: Double): Double{
        return a/b
    }
}